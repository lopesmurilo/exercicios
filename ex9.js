console.log("5" - 2);
// 3
// O operador de subtração (-) converte automaticamente a string "5" em número
// para conseguir realizar o cálculo matemático.

console.log("5" + 2);
// 52
// O operador +, encontra uma string, realiza concatenação
// em vez de soma numérica.

console.log(true + 1);
// 2
// O valor booleano true é convertido para o número 1 antes da soma.

console.log(false + 10);
// 10
// O valor booleano false é convertido para o número 0 antes da soma.